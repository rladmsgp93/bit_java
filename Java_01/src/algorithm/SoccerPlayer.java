package algorithm;

public class SoccerPlayer {
	String name;
	int age;

	public String getName() {
		return name;
	}

	public SoccerPlayer(String name, int age) {
		super();
		this.name = name;
		this.age = age;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
